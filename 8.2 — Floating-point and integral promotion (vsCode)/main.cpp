#include <iostream>

void printInt(int x)
{
    std::cout << x << '\n';
}

void printDouble(double d)
{
    std::cout << d << '\n';
}

int main()
{
    std::cout << std::endl;
    /////////////////////////////////////////////////////////////////////////////
    std::cout << "/////////////////////////////////////////////////////" << '\n';
    std::cout << "Numeric promotion" << '\n';
    std::cout << "/////////////////////////////////////////////////////" << '\n';
    /////////////////////////////////////////////////////////////////////////////

    std::cout << std::endl;
    /////////////////////////////////////////////////////////////////////////////
    std::cout << "/////////////////////////////////////////////////////" << '\n';
    std::cout << "Numeric promotion reduces redundancy" << '\n';
    std::cout << "/////////////////////////////////////////////////////" << '\n';
    /////////////////////////////////////////////////////////////////////////////

    std::cout << std::endl;
    /////////////////////////////////////////////////////////////////////////////
    std::cout << "/////////////////////////////////////////////////////" << '\n';
    std::cout << "Numeric promotion categories" << '\n';
    std::cout << "/////////////////////////////////////////////////////" << '\n';
    /////////////////////////////////////////////////////////////////////////////

    std::cout << std::endl;
    /////////////////////////////////////////////////////////////////////////////
    std::cout << "/////////////////////////////////////////////////////" << '\n';
    std::cout << "Floating point promotions" << '\n';
    std::cout << "/////////////////////////////////////////////////////" << '\n';
    /////////////////////////////////////////////////////////////////////////////

    printDouble(5.6);// no conversion necessary
    printDouble(4.1f);// numeric promotion of float to double

    //In the second call to printDouble(), the float literal 4.0f is promoted into a double.

    std::cout << std::endl;
    /////////////////////////////////////////////////////////////////////////////
    std::cout << "/////////////////////////////////////////////////////" << '\n';
    std::cout << "Integral promotions" << '\n';
    std::cout << "/////////////////////////////////////////////////////" << '\n';
    /////////////////////////////////////////////////////////////////////////////

    short s{ 3 };// there is no short literal suffix, so we'll use a variable for this one
    printInt(s);// numeric promotion of short to int
    printInt('a');// numeric promotion of char to int
    printInt(true);// numeric promotion of bool to int

    std::cout << std::endl;
    /////////////////////////////////////////////////////////////////////////////////////
    std::cout << "////////////////////////////////////////////////////////////" << '\n';
    std::cout << "Not all value-preserving conversions are numeric promotions" << '\n';
    std::cout << "////////////////////////////////////////////////////////////" << '\n';
    /////////////////////////////////////////////////////////////////////////////////////

    



    return 0;
}